#!/usr/bin/env bun
/**
 * Build-time codegen: reads VAULT-SCHEMA.md and emits
 * `src/vault/conventions/defaults.ts` with the canonical `_conventions.md`
 * body, per-note-type templates, and shared TS types.
 *
 * Usage: bun run generate:vault-schema
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

// Repo root is one level up from `scripts/`.
const REPO_ROOT = path.resolve(__dirname, '..')
// VAULT-SCHEMA.md lives in the workspace's .specs directory (one level above the bridgeai repo).
export const REPO_VAULT_SCHEMA_PATH = path.resolve(
  REPO_ROOT,
  '..',
  '.specs',
  'project',
  'VAULT-SCHEMA.md',
)
export const GENERATED_DEFAULTS_PATH = path.resolve(
  REPO_ROOT,
  'src',
  'vault',
  'conventions',
  'defaults.ts',
)

export const NOTE_TYPES = ['module', 'concept', 'flow', 'decision', 'incident', 'moc'] as const
export type NoteType = (typeof NOTE_TYPES)[number]

export interface ExtractedBlocks {
  schemaVersion: string
  conventionsMd: string
  templates: Record<NoteType, string>
}

function extractBetween(source: string, beginMarker: string, endMarker: string, label: string): string {
  const beginIdx = source.indexOf(beginMarker)
  const endIdx = source.indexOf(endMarker)
  if (beginIdx === -1 || endIdx === -1 || endIdx < beginIdx) {
    throw new Error(`Missing marker block for ${label}: expected ${beginMarker} ... ${endMarker}`)
  }
  const start = beginIdx + beginMarker.length
  // Strip exactly one leading newline and one trailing newline if present.
  let block = source.slice(start, endIdx)
  if (block.startsWith('\n')) block = block.slice(1)
  if (block.endsWith('\n')) block = block.slice(0, -1)
  return block
}

export function extractBlocks(source: string): ExtractedBlocks {
  const versionMatch = source.match(/<!--\s*SCHEMA-VERSION:\s*([^\s]+)\s*-->/)
  if (!versionMatch) {
    throw new Error('Missing <!-- SCHEMA-VERSION: x.y.z --> marker in VAULT-SCHEMA.md')
  }
  const schemaVersion = versionMatch[1]!

  const conventionsMd = extractBetween(
    source,
    '<!-- CONVENTIONS-MD-BEGIN -->',
    '<!-- CONVENTIONS-MD-END -->',
    'conventions',
  )

  const templates = {} as Record<NoteType, string>
  for (const t of NOTE_TYPES) {
    const upper = t.toUpperCase()
    templates[t] = extractBetween(
      source,
      `<!-- TEMPLATE-${upper}-BEGIN -->`,
      `<!-- TEMPLATE-${upper}-END -->`,
      t,
    )
  }

  return { schemaVersion, conventionsMd, templates }
}

/**
 * Emits a string as a TypeScript backtick-template literal.
 * Escapes backslashes, backticks, and `${` interpolation sequences.
 */
function toBacktickLiteral(s: string): string {
  const escaped = s
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${')
  return '`' + escaped + '`'
}

export function renderDefaultsFile(blocks: ExtractedBlocks): string {
  const header = `// AUTO-GENERATED by scripts/generate-vault-schema-defaults.ts. Do not edit by hand. Run: bun run generate:vault-schema\n`

  const types = `
export const NOTE_TYPES = ['module', 'concept', 'flow', 'decision', 'incident', 'moc'] as const
export type NoteType = (typeof NOTE_TYPES)[number]

export const NOTE_STATUSES = [
  'draft',
  'active',
  'review',
  'stable',
  'deprecated',
  'archived',
] as const
export type NoteStatus = (typeof NOTE_STATUSES)[number]

export const CONFIDENCES = ['high', 'medium', 'low', 'speculative'] as const
export type Confidence = (typeof CONFIDENCES)[number]

export interface TypeRules {
  requiredFields: string[]
  sections: string[]
}

export interface ConventionSchema {
  schemaVersion: string
  requiredFrontmatter: string[]
  noteTypes: Record<NoteType, TypeRules>
  tagTaxonomy: { prefixes: string[]; minCount: number; maxCount: number }
  naming: { casing: 'kebab'; prefixByType: Record<NoteType, string> }
  sizeLimits: Record<NoteType, { minTokens: number; maxTokens: number }>
}
`

  const schemaVersionLine = `export const SCHEMA_VERSION = ${JSON.stringify(blocks.schemaVersion)}\n`

  const conventionsLine = `export const CONVENTIONS_MD_DEFAULT: string = ${toBacktickLiteral(blocks.conventionsMd)}\n`

  const templateEntries = NOTE_TYPES.map(
    (t) => `  ${t}: ${toBacktickLiteral(blocks.templates[t])},`,
  ).join('\n')
  const templatesLine = `export const TEMPLATES: Record<NoteType, string> = {\n${templateEntries}\n}\n`

  return [header, types, schemaVersionLine, conventionsLine, templatesLine].join('\n')
}

function main(): void {
  const source = readFileSync(REPO_VAULT_SCHEMA_PATH, 'utf8')
  const blocks = extractBlocks(source)
  const rendered = renderDefaultsFile(blocks)
  mkdirSync(path.dirname(GENERATED_DEFAULTS_PATH), { recursive: true })
  writeFileSync(GENERATED_DEFAULTS_PATH, rendered, 'utf8')
  // eslint-disable-next-line no-console
  console.log(`Wrote ${GENERATED_DEFAULTS_PATH} (schema ${blocks.schemaVersion})`)
}

// Only run main() when executed directly, not when imported by tests.
// Bun sets import.meta.main when run as entry point.
if ((import.meta as { main?: boolean }).main) {
  main()
}
