#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
cd "$ROOT_DIR"

PORT="${1:-8741}"
API_KEY="${2:-worker-${PORT}-change-me}"
NAME="${3:-openclaude-agent-${PORT}}"
ENV_FILE="${4:-.env}"
DRY_RUN="${5:-}"
RUNNER_CWD="${OPENCLAUDE_AGENT_RUNNER_CWD:-/workspace}"
RUNNER_MAX_TURNS="${OPENCLAUDE_AGENT_RUNNER_MAX_TURNS:-8}"
RUNNER_TIMEOUT_MS="${OPENCLAUDE_AGENT_RUNNER_TIMEOUT_MS:-120000}"
RUNNER_PERMISSION_MODE="${OPENCLAUDE_AGENT_RUNNER_PERMISSION_MODE:-bypassPermissions}"
RUNNER_DISALLOWED_TOOLS="${OPENCLAUDE_AGENT_RUNNER_DISALLOWED_TOOLS:-}"
MCPR_HOST_VALUE="${MCPR_HOST:-host.docker.internal}"
MCPR_PORT_VALUE="${MCPR_PORT:-3282}"
OPENRAG_URL_VALUE="${OPENRAG_URL:-http://host.docker.internal:3000}"
OPENRAG_MCP_TIMEOUT_VALUE="${OPENRAG_MCP_TIMEOUT:-60}"
CAMOFOX_URL_VALUE="${CAMOFOX_URL:-http://host.docker.internal:9377}"
CAMOFOX_MCP_TIMEOUT_VALUE="${CAMOFOX_MCP_TIMEOUT:-60}"
HINDSIGHT_URL_VALUE="${HINDSIGHT_URL:-http://host.docker.internal:8888}"
HINDSIGHT_BANK_ID_VALUE="${HINDSIGHT_BANK_ID:-openclaude-agent}"
HINDSIGHT_MCP_TIMEOUT_VALUE="${HINDSIGHT_MCP_TIMEOUT:-60}"
REPLICA_CRON_ENABLED="${OPENCLAUDE_REPLICA_CRON_ENABLED:-0}"
REPLICA_CRON_TICK_SECONDS="${OPENCLAUDE_REPLICA_CRON_TICK_SECONDS:-60}"
REPLICA_TELEGRAM_ENABLED="${OPENCLAUDE_REPLICA_TELEGRAM_ENABLED:-0}"
REPLICA_TELEGRAM_BOT_TOKEN="${OPENCLAUDE_REPLICA_TELEGRAM_BOT_TOKEN:-${TELEGRAM_BOT_TOKEN:-}}"
REPLICA_TELEGRAM_HOME_CHAT_ID="${OPENCLAUDE_REPLICA_TELEGRAM_HOME_CHAT_ID:-${OPENCLAUDE_TELEGRAM_HOME_CHAT_ID:-}}"
REPLICA_TELEGRAM_ALLOWED_CHAT_IDS="${OPENCLAUDE_REPLICA_TELEGRAM_ALLOWED_CHAT_IDS:-${OPENCLAUDE_TELEGRAM_ALLOWED_CHAT_IDS:-}}"
REPLICA_TELEGRAM_ALLOWED_USER_IDS="${OPENCLAUDE_REPLICA_TELEGRAM_ALLOWED_USER_IDS:-${OPENCLAUDE_TELEGRAM_ALLOWED_USER_IDS:-}}"
REPLICA_OUROBOROS_ENABLED="${OPENCLAUDE_REPLICA_OUROBOROS_ENABLED:-0}"
REPLICA_CONSCIOUSNESS_ENABLED="${OPENCLAUDE_REPLICA_CONSCIOUSNESS_ENABLED:-0}"
REPLICA_INFINITE_TASKS_ENABLED="${OPENCLAUDE_REPLICA_INFINITE_TASKS_ENABLED:-0}"
REPLICA_OUROBOROS_WAKEUP_MIN_SECONDS="${OPENCLAUDE_REPLICA_OUROBOROS_WAKEUP_MIN_SECONDS:-300}"
REPLICA_OUROBOROS_WAKEUP_MAX_SECONDS="${OPENCLAUDE_REPLICA_OUROBOROS_WAKEUP_MAX_SECONDS:-7200}"
REPLICA_OUROBOROS_MAX_ROUNDS="${OPENCLAUDE_REPLICA_OUROBOROS_MAX_ROUNDS:-3}"
REPLICA_OUROBOROS_BUDGET_FRACTION="${OPENCLAUDE_REPLICA_OUROBOROS_BUDGET_FRACTION:-0.1}"
WEB_SEARCH_PROVIDER_VALUE="${WEB_SEARCH_PROVIDER:-auto}"
PROVIDER_SUMMARY="${OPENCLAUDE_PROVIDER:-${CLAUDE_CODE_USE_OPENAI:+openai}${CLAUDE_CODE_USE_GEMINI:+gemini}${CLAUDE_CODE_USE_MISTRAL:+mistral}${CLAUDE_CODE_USE_GITHUB:+github}}"
PROVIDER_MODEL_SUMMARY="${OPENCLAUDE_MODEL:-${OPENAI_MODEL:-${GEMINI_MODEL:-${MISTRAL_MODEL:-${ANTHROPIC_MODEL:-}}}}}"

ENV_FILE_ARGS=()
if [[ -f "$ENV_FILE" ]]; then
  ENV_FILE_ARGS=(--env-file "$ENV_FILE")
fi

PROVIDER_ENV_ARGS=()
add_provider_env_if_set() {
  local name="$1"
  local value="${!name-}"
  if [[ -n "$value" ]]; then
    PROVIDER_ENV_ARGS+=(-e "${name}=${value}")
  fi
}

for provider_env_name in \
  CLAUDE_CODE_USE_OPENAI CLAUDE_CODE_USE_GEMINI CLAUDE_CODE_USE_MISTRAL \
  CLAUDE_CODE_USE_GITHUB CLAUDE_CODE_USE_BEDROCK CLAUDE_CODE_USE_VERTEX \
  CLAUDE_CODE_USE_FOUNDRY OPENCLAUDE_PROVIDER OPENCLAUDE_BASE_URL \
  OPENCLAUDE_MODEL OPENCLAUDE_API_KEY OPENAI_BASE_URL OPENAI_MODEL \
  OPENAI_API_KEY CODEX_API_KEY CODEX_CREDENTIAL_SOURCE CHATGPT_ACCOUNT_ID \
  CODEX_ACCOUNT_ID ANTHROPIC_BASE_URL ANTHROPIC_MODEL ANTHROPIC_API_KEY \
  GEMINI_BASE_URL GEMINI_MODEL GEMINI_API_KEY GOOGLE_API_KEY MISTRAL_BASE_URL \
  MISTRAL_MODEL MISTRAL_API_KEY; do
  add_provider_env_if_set "$provider_env_name"
done

if [[ "$DRY_RUN" == "--dry-run" ]]; then
  echo "Replica dry run"
  echo "Port: ${PORT}"
  echo "API key: ${API_KEY}"
  echo "Container: ${NAME}"
  echo "Env file: ${ENV_FILE}"
  echo "Telegram enabled: ${REPLICA_TELEGRAM_ENABLED}"
  echo "Cron enabled: ${REPLICA_CRON_ENABLED}"
  echo "Cron tick seconds: ${REPLICA_CRON_TICK_SECONDS}"
  echo "Ouroboros enabled: ${REPLICA_OUROBOROS_ENABLED}"
  echo "Consciousness enabled: ${REPLICA_CONSCIOUSNESS_ENABLED}"
  echo "Infinite tasks enabled: ${REPLICA_INFINITE_TASKS_ENABLED}"
  echo "Ouroboros wakeup: ${REPLICA_OUROBOROS_WAKEUP_MIN_SECONDS}-${REPLICA_OUROBOROS_WAKEUP_MAX_SECONDS}s"
  echo "Ouroboros max rounds: ${REPLICA_OUROBOROS_MAX_ROUNDS}"
  echo "Ouroboros budget fraction: ${REPLICA_OUROBOROS_BUDGET_FRACTION}"
  echo "Runner cwd: ${RUNNER_CWD}"
  echo "Runner blocked tools: ${RUNNER_DISALLOWED_TOOLS}"
  echo "Provider: ${PROVIDER_SUMMARY:-default}"
  echo "Provider model: ${PROVIDER_MODEL_SUMMARY:-default}"
  echo "WebSearch provider: ${WEB_SEARCH_PROVIDER_VALUE}"
  echo "MCP Router: ${MCPR_HOST_VALUE}:${MCPR_PORT_VALUE}"
  echo "OpenRAG: ${OPENRAG_URL_VALUE}"
  echo "Camofox: ${CAMOFOX_URL_VALUE}"
  echo "Hindsight: ${HINDSIGHT_URL_VALUE} bank=${HINDSIGHT_BANK_ID_VALUE}"
  exit 0
fi

docker build -t openclaude-agent:local .
docker rm -f "$NAME" >/dev/null 2>&1 || true

docker run -d \
  --name "$NAME" \
  "${ENV_FILE_ARGS[@]}" \
  "${PROVIDER_ENV_ARGS[@]}" \
  --add-host host.docker.internal:host-gateway \
  -p "${PORT}:${PORT}" \
  -e OPENCLAUDE_AGENT_API_ENABLED=1 \
  -e OPENCLAUDE_AGENT_API_HOST=0.0.0.0 \
  -e OPENCLAUDE_AGENT_API_PORT="$PORT" \
  -e OPENCLAUDE_AGENT_API_KEY="$API_KEY" \
  -e OPENCLAUDE_AGENT_CRON_ENABLED="$REPLICA_CRON_ENABLED" \
  -e OPENCLAUDE_AGENT_CRON_TICK_SECONDS="$REPLICA_CRON_TICK_SECONDS" \
  -e OPENCLAUDE_TELEGRAM_ENABLED="$REPLICA_TELEGRAM_ENABLED" \
  -e TELEGRAM_BOT_TOKEN="$REPLICA_TELEGRAM_BOT_TOKEN" \
  -e OPENCLAUDE_TELEGRAM_HOME_CHAT_ID="$REPLICA_TELEGRAM_HOME_CHAT_ID" \
  -e OPENCLAUDE_TELEGRAM_ALLOWED_CHAT_IDS="$REPLICA_TELEGRAM_ALLOWED_CHAT_IDS" \
  -e OPENCLAUDE_TELEGRAM_ALLOWED_USER_IDS="$REPLICA_TELEGRAM_ALLOWED_USER_IDS" \
  -e OPENCLAUDE_OUROBOROS_ENABLED="$REPLICA_OUROBOROS_ENABLED" \
  -e OPENCLAUDE_CONSCIOUSNESS_ENABLED="$REPLICA_CONSCIOUSNESS_ENABLED" \
  -e OPENCLAUDE_INFINITE_TASKS_ENABLED="$REPLICA_INFINITE_TASKS_ENABLED" \
  -e OPENCLAUDE_OUROBOROS_WAKEUP_MIN_SECONDS="$REPLICA_OUROBOROS_WAKEUP_MIN_SECONDS" \
  -e OPENCLAUDE_OUROBOROS_WAKEUP_MAX_SECONDS="$REPLICA_OUROBOROS_WAKEUP_MAX_SECONDS" \
  -e OPENCLAUDE_OUROBOROS_MAX_ROUNDS="$REPLICA_OUROBOROS_MAX_ROUNDS" \
  -e OPENCLAUDE_OUROBOROS_BUDGET_FRACTION="$REPLICA_OUROBOROS_BUDGET_FRACTION" \
  -e OPENCLAUDE_AGENT_RUNNER_CWD="$RUNNER_CWD" \
  -e OPENCLAUDE_AGENT_RUNNER_MAX_TURNS="$RUNNER_MAX_TURNS" \
  -e OPENCLAUDE_AGENT_RUNNER_TIMEOUT_MS="$RUNNER_TIMEOUT_MS" \
  -e OPENCLAUDE_AGENT_RUNNER_PERMISSION_MODE="$RUNNER_PERMISSION_MODE" \
  -e OPENCLAUDE_AGENT_RUNNER_DISALLOWED_TOOLS="$RUNNER_DISALLOWED_TOOLS" \
  -e WEB_SEARCH_PROVIDER="$WEB_SEARCH_PROVIDER_VALUE" \
  -e WEB_PROVIDER="${WEB_PROVIDER:-}" \
  -e WEB_KEY="${WEB_KEY:-}" \
  -e WEB_SEARCH_API="${WEB_SEARCH_API:-}" \
  -e WEB_QUERY_PARAM="${WEB_QUERY_PARAM:-}" \
  -e WEB_METHOD="${WEB_METHOD:-}" \
  -e WEB_PARAMS="${WEB_PARAMS:-}" \
  -e WEB_URL_TEMPLATE="${WEB_URL_TEMPLATE:-}" \
  -e WEB_BODY_TEMPLATE="${WEB_BODY_TEMPLATE:-}" \
  -e WEB_AUTH_HEADER="${WEB_AUTH_HEADER:-}" \
  -e WEB_AUTH_SCHEME="${WEB_AUTH_SCHEME:-}" \
  -e WEB_HEADERS="${WEB_HEADERS:-}" \
  -e WEB_JSON_PATH="${WEB_JSON_PATH:-}" \
  -e WEB_CUSTOM_TIMEOUT_SEC="${WEB_CUSTOM_TIMEOUT_SEC:-}" \
  -e WEB_CUSTOM_MAX_BODY_KB="${WEB_CUSTOM_MAX_BODY_KB:-}" \
  -e WEB_CUSTOM_ALLOW_ARBITRARY_HEADERS="${WEB_CUSTOM_ALLOW_ARBITRARY_HEADERS:-}" \
  -e WEB_CUSTOM_ALLOW_HTTP="${WEB_CUSTOM_ALLOW_HTTP:-}" \
  -e WEB_CUSTOM_ALLOW_PRIVATE="${WEB_CUSTOM_ALLOW_PRIVATE:-}" \
  -e FIRECRAWL_API_KEY="${FIRECRAWL_API_KEY:-}" \
  -e TAVILY_API_KEY="${TAVILY_API_KEY:-}" \
  -e EXA_API_KEY="${EXA_API_KEY:-}" \
  -e YOU_API_KEY="${YOU_API_KEY:-}" \
  -e JINA_API_KEY="${JINA_API_KEY:-}" \
  -e BING_API_KEY="${BING_API_KEY:-}" \
  -e MOJEEK_API_KEY="${MOJEEK_API_KEY:-}" \
  -e LINKUP_API_KEY="${LINKUP_API_KEY:-}" \
  -e MCPR_HOST="$MCPR_HOST_VALUE" \
  -e MCPR_PORT="$MCPR_PORT_VALUE" \
  -e OPENRAG_URL="$OPENRAG_URL_VALUE" \
  -e OPENRAG_API_KEY="${OPENRAG_API_KEY:-}" \
  -e OPENRAG_MCP_TIMEOUT="$OPENRAG_MCP_TIMEOUT_VALUE" \
  -e CAMOFOX_URL="$CAMOFOX_URL_VALUE" \
  -e CAMOFOX_ACCESS_KEY="${CAMOFOX_ACCESS_KEY:-}" \
  -e CAMOFOX_API_KEY="${CAMOFOX_API_KEY:-}" \
  -e CAMOFOX_MCP_TIMEOUT="$CAMOFOX_MCP_TIMEOUT_VALUE" \
  -e HINDSIGHT_URL="$HINDSIGHT_URL_VALUE" \
  -e HINDSIGHT_API_KEY="${HINDSIGHT_API_KEY:-}" \
  -e HINDSIGHT_BANK_ID="$HINDSIGHT_BANK_ID_VALUE" \
  -e HINDSIGHT_MCP_TIMEOUT="$HINDSIGHT_MCP_TIMEOUT_VALUE" \
  -v "$ROOT_DIR:/workspace" \
  -v "${NAME}-config:/home/node/.openclaude" \
  -w /workspace \
  openclaude-agent:local --agent-gateway-server

echo "Replica started: http://127.0.0.1:${PORT}/v1"
echo "API key: ${API_KEY}"
echo "Container: ${NAME}"
echo "Env file: ${ENV_FILE}"
echo "Telegram enabled: ${REPLICA_TELEGRAM_ENABLED}"
