import type { NoteDraft } from '../../conventions/validator.js'
import type { ModuleDescriptor } from '../types.js'

const AUTO_COMMENT = '<!-- AUTO-GENERATED by bridgeai map. Hand edits are overwritten. -->'

export interface MocResult {
  root: NoteDraft
  perDomain: NoteDraft[]
  /** Maps each module slug → array of MOC filenames that link to it. */
  backEdges: Map<string, string[]>
}

/**
 * Generate Maps of Content (MOCs) from enriched module descriptors.
 *
 * - One root `moc-codebase` listing every domain with WikiLinks to `[[moc-<domain>]]`.
 * - One `moc-<domain>` per unique domain, listing modules alphabetically
 *   with WikiLinks and one-line summaries.
 * - `backEdges`: each module gains a reference to its domain MOC filename
 *   (used by the pipeline to patch `related` on module notes).
 */
export function generateMocs(descriptors: ModuleDescriptor[]): MocResult {
  const today = new Date().toISOString().slice(0, 10)
  const backEdges = new Map<string, string[]>()

  // Initialize backEdges for all descriptors
  for (const d of descriptors) {
    backEdges.set(d.slug, [])
  }

  // Group by domain
  const byDomain = new Map<string, ModuleDescriptor[]>()
  for (const d of descriptors) {
    const list = byDomain.get(d.domain) ?? []
    list.push(d)
    byDomain.set(d.domain, list)
  }

  // Sort domains alphabetically
  const sortedDomains = [...byDomain.keys()].sort()

  // Collect all pending link filenames (all modules + all MOCs we'll create)
  const allPending: string[] = []
  for (const d of descriptors) {
    allPending.push(`module-${d.slug}`)
  }
  for (const domain of sortedDomains) {
    allPending.push(`moc-${domain}`)
  }
  allPending.push('moc-codebase')

  // Generate per-domain MOCs
  const perDomain: NoteDraft[] = []
  for (const domain of sortedDomains) {
    const modules = byDomain.get(domain)!.sort((a, b) => a.slug.localeCompare(b.slug))
    const mocFilename = `moc-${domain}`

    // Build body
    const moduleList = modules
      .map((m) => `- [[module-${m.slug}]] — ${m.summary}`)
      .join('\n')

    const body = [
      AUTO_COMMENT,
      '',
      `> **TL;DR**: Map of Content for the **${domain}** domain.`,
      '',
      '## Key modules',
      '',
      moduleList,
      '',
      '## Key flows',
      '',
      '_No flows mapped yet._',
      '',
      '## Key decisions',
      '',
      '_No decisions mapped yet._',
      '',
      '## Related concepts',
      '',
      '_No concepts mapped yet._',
    ].join('\n')

    const tags = buildMocTags(domain)

    const draft: NoteDraft = {
      filename: mocFilename,
      folder: 'maps',
      frontmatter: {
        title: `${domain} domain`,
        type: 'moc',
        status: 'draft',
        created: today,
        updated: today,
        confidence: 'high',
        summary: `Map of Content for the ${domain} domain.`,
        tags,
        related: ['[[moc-codebase]]'],
        _pendingLinks: allPending,
      },
      body,
    }

    perDomain.push(draft)

    // Record backEdges: each module in this domain gets a link from this MOC
    for (const m of modules) {
      const edges = backEdges.get(m.slug)!
      edges.push(mocFilename)
    }
  }

  // Generate root MOC
  const domainList = sortedDomains
    .map((d) => {
      const count = byDomain.get(d)!.length
      return `- [[moc-${d}]] — ${count} module${count !== 1 ? 's' : ''}`
    })
    .join('\n')

  const rootBody = [
    AUTO_COMMENT,
    '',
    `> **TL;DR**: Root map of the codebase — ${descriptors.length} modules across ${sortedDomains.length} domains.`,
    '',
    '## Key modules',
    '',
    domainList,
    '',
    '## Key flows',
    '',
    '_No flows mapped yet._',
    '',
    '## Key decisions',
    '',
    '_No decisions mapped yet._',
    '',
    '## Related concepts',
    '',
    '_No concepts mapped yet._',
  ].join('\n')

  const root: NoteDraft = {
    filename: 'moc-codebase',
    folder: 'maps',
    frontmatter: {
      title: 'Codebase',
      type: 'moc',
      status: 'draft',
      created: today,
      updated: today,
      confidence: 'high',
      summary: `Root map of the codebase — ${descriptors.length} modules across ${sortedDomains.length} domains.`,
      tags: ['code/architecture', 'code/module', 'domain/codebase'],
      related: sortedDomains.map((d) => `[[moc-${d}]]`),
      _pendingLinks: allPending,
    },
    body: rootBody,
  }

  return { root, perDomain, backEdges }
}

function buildMocTags(domain: string): string[] {
  return ['code/architecture', 'code/module', `domain/${domain}`]
}
