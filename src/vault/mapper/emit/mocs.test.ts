import { describe, test, expect } from 'bun:test'
import { generateMocs } from './mocs.js'
import type { ModuleDescriptor } from '../types.js'

function desc(slug: string, domain: string, summary = `${slug} module`): ModuleDescriptor {
  return {
    slug,
    sourcePath: `/repo/src/${slug}`,
    files: [],
    language: 'typescript',
    exports: ['default'],
    dependsOn: [],
    dependedBy: [],
    externals: [],
    summary,
    responsibilities: ['Does things'],
    domain,
    layer: 'service',
    fallback: false,
    staticOnly: false,
  }
}

describe('generateMocs', () => {
  const descriptors = [
    desc('auth-handler', 'auth', 'Handles authentication'),
    desc('auth-middleware', 'auth', 'JWT middleware'),
    desc('user-service', 'users', 'User CRUD operations'),
    desc('email-sender', 'notifications', 'Sends email notifications'),
    desc('push-sender', 'notifications', 'Sends push notifications'),
  ]

  test('root MOC lists every unique domain; count matches', () => {
    const result = generateMocs(descriptors)

    expect(result.root.filename).toBe('moc-codebase')
    expect(result.root.folder).toBe('maps')
    expect(result.root.body).toContain('[[moc-auth]]')
    expect(result.root.body).toContain('[[moc-users]]')
    expect(result.root.body).toContain('[[moc-notifications]]')
    // 3 unique domains
    expect((result.root.frontmatter.related as string[]).length).toBe(3)
  })

  test('per-domain MOC lists every module with that domain, alphabetical', () => {
    const result = generateMocs(descriptors)

    const authMoc = result.perDomain.find((m) => m.filename === 'moc-auth')!
    expect(authMoc).toBeDefined()
    expect(authMoc.body).toContain('[[module-auth-handler]]')
    expect(authMoc.body).toContain('[[module-auth-middleware]]')

    // Verify alphabetical: auth-handler before auth-middleware
    const handlerIdx = authMoc.body.indexOf('[[module-auth-handler]]')
    const middlewareIdx = authMoc.body.indexOf('[[module-auth-middleware]]')
    expect(handlerIdx).toBeLessThan(middlewareIdx)

    const notifMoc = result.perDomain.find((m) => m.filename === 'moc-notifications')!
    expect(notifMoc.body).toContain('[[module-email-sender]]')
    expect(notifMoc.body).toContain('[[module-push-sender]]')
  })

  test('backEdges map has entry for every descriptor with exactly one moc-<domain>', () => {
    const result = generateMocs(descriptors)

    expect(result.backEdges.size).toBe(descriptors.length)
    expect(result.backEdges.get('auth-handler')).toEqual(['moc-auth'])
    expect(result.backEdges.get('auth-middleware')).toEqual(['moc-auth'])
    expect(result.backEdges.get('user-service')).toEqual(['moc-users'])
    expect(result.backEdges.get('email-sender')).toEqual(['moc-notifications'])
  })

  test('auto-generated comment appears in every MOC body', () => {
    const result = generateMocs(descriptors)

    expect(result.root.body).toContain('AUTO-GENERATED by bridgeai map')
    for (const moc of result.perDomain) {
      expect(moc.body).toContain('AUTO-GENERATED by bridgeai map')
    }
  })

  test('related back-link from domain MOC to root MOC is present', () => {
    const result = generateMocs(descriptors)

    for (const moc of result.perDomain) {
      const related = moc.frontmatter.related as string[]
      expect(related).toContain('[[moc-codebase]]')
    }
  })

  test('root MOC summary includes module and domain counts', () => {
    const result = generateMocs(descriptors)

    expect(result.root.frontmatter.summary).toContain('5 modules')
    expect(result.root.frontmatter.summary).toContain('3 domains')
  })

  test('per-domain MOC includes module summaries', () => {
    const result = generateMocs(descriptors)

    const usersMoc = result.perDomain.find((m) => m.filename === 'moc-users')!
    expect(usersMoc.body).toContain('User CRUD operations')
  })

  test('single-domain input produces one domain MOC + root', () => {
    const single = [desc('only-mod', 'core')]
    const result = generateMocs(single)

    expect(result.perDomain).toHaveLength(1)
    expect(result.perDomain[0].filename).toBe('moc-core')
    expect(result.root.body).toContain('[[moc-core]]')
    expect(result.root.body).toContain('1 module')
  })
})
