import { existsSync, readFileSync, writeFileSync, mkdirSync, readdirSync } from 'fs'
import { join } from 'path'
import { findGitRoot } from '../utils/git.js'
import { ensureIgnored } from '../utils/git/repoGitignore.js'
import {
  CONVENTIONS_MD_DEFAULT,
  TEMPLATES,
  NOTE_TYPES,
} from './conventions/defaults.js'
import type { VaultConfig } from './types.js'

export type BootstrapResult = {
  vaultPath: string
  filesCreated: string[]
  gitignoreUpdated: boolean
}

export type RegenerateResult = {
  filesWritten: string[]
}

export type VaultShape = 'none' | 'v1' | 'v2'

const SUBFOLDERS = [
  'knowledge',
  'maps',
  'decisions',
  'flows',
  'incidents',
  'archive',
] as const

/** Write `content` to `absPath` only if the file does not already exist. Returns true if written. */
function writeIfAbsent(absPath: string, content: string): boolean {
  if (existsSync(absPath)) return false
  mkdirSync(join(absPath, '..'), { recursive: true })
  writeFileSync(absPath, content, 'utf-8')
  return true
}

function masterIndex(): string {
  const entries = SUBFOLDERS.map((f) => `- [[${f}/_index]]`).join('\n')
  return `# Vault

> Master catalog. Auto-generated by bridge-ai bootstrap.

## Folders

${entries}

## Meta

- [[_conventions]] — vault constitution
- [[_log]] — append-only mutation log
- \`meta/templates/\` — per-type note templates
`
}

function subfolderIndex(name: string): string {
  return `# ${name}

> _No notes yet._
`
}

function initialLog(): string {
  const ts = new Date().toISOString()
  return `# Vault log

- ${ts}  bootstrap  source: code-analysis
`
}

function appendLogEntry(vaultPath: string, kind: string): void {
  const ts = new Date().toISOString()
  const line = `- ${ts}  ${kind}  source: code-analysis\n`
  const logPath = join(vaultPath, '_log.md')
  if (!existsSync(logPath)) {
    writeFileSync(logPath, `# Vault log\n\n${line}`, 'utf-8')
    return
  }
  const content = readFileSync(logPath, 'utf-8')
  const needsNl = content.length > 0 && !content.endsWith('\n')
  writeFileSync(logPath, content + (needsNl ? '\n' : '') + line, 'utf-8')
}

/**
 * Materialize the VAULT-SCHEMA.md §2 tree at `cfg.vaultPath`.
 *
 * - Refuses to run outside a git repository.
 * - Idempotent: existing files are never overwritten.
 * - When `opts.gitignore` is true, ensures `.bridgeai/` is listed in `<repoRoot>/.gitignore`.
 */
export async function bootstrapVault(
  cfg: VaultConfig,
  opts: { gitignore: boolean },
): Promise<BootstrapResult> {
  const repoRoot = findGitRoot(cfg.projectRoot)
  if (!repoRoot) {
    throw new Error('Cannot bootstrap vault outside a git repository')
  }

  const vp = cfg.vaultPath
  mkdirSync(vp, { recursive: true })

  const filesCreated: string[] = []
  const track = (relPath: string, wrote: boolean) => {
    if (wrote) filesCreated.push(relPath)
  }

  // Top-level files
  track('_index.md', writeIfAbsent(join(vp, '_index.md'), masterIndex()))
  track(
    '_conventions.md',
    writeIfAbsent(join(vp, '_conventions.md'), CONVENTIONS_MD_DEFAULT),
  )
  track('_log.md', writeIfAbsent(join(vp, '_log.md'), initialLog()))

  // Subfolder indexes
  for (const f of SUBFOLDERS) {
    track(
      `${f}/_index.md`,
      writeIfAbsent(join(vp, f, '_index.md'), subfolderIndex(f)),
    )
  }

  // Templates
  for (const type of NOTE_TYPES) {
    track(
      `meta/templates/${type}.md`,
      writeIfAbsent(join(vp, 'meta', 'templates', `${type}.md`), TEMPLATES[type]),
    )
  }

  let gitignoreUpdated = false
  if (opts.gitignore) {
    const { added } = ensureIgnored(repoRoot, '.bridgeai/')
    gitignoreUpdated = added
  }

  return { vaultPath: vp, filesCreated, gitignoreUpdated }
}

/**
 * Classify a vault directory:
 * - `none`: missing or lacks both `manifest.json` and `_conventions.md`
 * - `v1`: has `manifest.json` but no `_conventions.md`
 * - `v2`: has `_conventions.md`
 */
export function detectVaultShape(vaultPath: string): VaultShape {
  if (!existsSync(vaultPath)) return 'none'
  let entries: string[]
  try {
    entries = readdirSync(vaultPath)
  } catch {
    return 'none'
  }

  const hasConventions = entries.includes('_conventions.md')
  const hasManifest = entries.includes('manifest.json')

  if (hasConventions) return 'v2'
  if (hasManifest) return 'v1'
  return 'none'
}

export function isVaultV2Bootstrapped(vaultPath: string): boolean {
  return detectVaultShape(vaultPath) === 'v2'
}

/**
 * Rewrite `_conventions.md` and all `meta/templates/<type>.md` to their default
 * content, append a `conventions-regenerated` entry to `_log.md`, and leave
 * all notes untouched.
 */
export async function regenerateConventions(
  cfg: VaultConfig,
): Promise<RegenerateResult> {
  const vp = cfg.vaultPath
  const filesWritten: string[] = []

  mkdirSync(vp, { recursive: true })

  const conventionsPath = join(vp, '_conventions.md')
  writeFileSync(conventionsPath, CONVENTIONS_MD_DEFAULT, 'utf-8')
  filesWritten.push('_conventions.md')

  const tplDir = join(vp, 'meta', 'templates')
  mkdirSync(tplDir, { recursive: true })
  for (const type of NOTE_TYPES) {
    const p = join(tplDir, `${type}.md`)
    writeFileSync(p, TEMPLATES[type], 'utf-8')
    filesWritten.push(`meta/templates/${type}.md`)
  }

  appendLogEntry(vp, 'conventions-regenerated')

  return { filesWritten }
}
